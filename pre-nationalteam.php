            <h2>ZEIT VOR DER MÄNNER-NATIONALMANNSCHAFT</h2>


            <p>Bereits mit 7 Jahren begann Alexander Wieczerzak die Kampfsportart Judo zu erlernen. Nachdem sich die Sportart für ihn als interessant erwies, trainierte der junge Athlet ab dem Jahr 2001 in dem Verein Frankfurter TV 1860 unter dem
              Betreuer Lutz Befuß. Von Zukunftsperspektiven geleitet, wechselte er nach 6 Jahren in den Judo Club Rüsselsheim. Der Heimtrainer Andreas Esper führte den Sportler erfolgreich in die 1.Bundesliga. Nach mehrfachen Erfolgen auf
              Ranglisten-Turnieren wurde Alexander Wieczerzak in der Gewichtsklasse -66 Kg unter der Betreuung vom Landestrainer Patrick Kuptz, Deutscher Meister. Nach weiteren erfolgreichen nationalen und internationalen Turnieren, schaffte
              Alexander sich ein Platz in der deutschen Nationalmannschaft zu sichern und flog 2007 auf seine erste Junior Europameisterschaft nach Malta. Unter zu hohem Druck, lief der Wettkampftag nicht wie es sich der junge Athlet vorstellt.</p>
            <p>Im Dezember 2009 wechselte der Judoka zum Judo Club Wiesbaden 1922 e.V., hier arbeitet Alexander wieder eng mit seinem Trainer Patrick Kuptz zusammen. Und auch dort etablierte er sich als Punktegarant im Bundesligateam. Auch JC
              Trainer Patrick Neputh und Philipp Eckelmann gehören zu Alexanders großen Stützen, deren Organisation und Kooperation dem Sportler eine große Last von den Schultern nehmen. Nach einer langwierigen Pause bedingt durch Verletzung, fand
              Alexander bei den Deutschen Meisterschaften 2010 den Anschluss und wurde Deutscher Meister -73 kg in der Altersklasse U20. Wie sich im Nachhinein herausstellte ein sehr erfolgreiches Jahr für Alexander Wieczerzak.</p>
            <p>Nach unzähligen Turnieren und Wettkämpfen rund um den Globus unter anderem in Italien, Portugal, Usbekistan und Korea, machte sich jede Anstrengung für bezahlt. In Topform qualifizierte Alexander sich durch die internationalen Siege
              für die Weltmeisterschaft. Bei dieser Weltmeisterschaft gingen 597 Athleten aus 78 Nationen auf die Matte und in seiner Gewichtsklasse -73 Kilogramm waren es 47 Starter aus 43 Nationen, die sich Hoffnungen auf den Sieg machten. Im
              Vorrundenkampf wurde Wieczerzak gleich mit dem Georgier Lasha Zurabiani konfrontiert, Bronzemedaillengewinner der letzten Weltmeisterschaft. Darauf folgten weiter kämpfe mit dem Ukrainer Kanivets Dmytro, dem Amerikaner Porras Andrew,
              dem Kasachstaner Makhambetov Arman, dem Afrikameister aus Tunesien Barhoumi Hamza und letztendlich mit seinem Team-Kollegen Hannes Conrad aus Leipzig. Durch die Leistungssteigerung von Kampf zu Kampf, gewann er Willensstärke und
              Sicherheit und letztlich den Höhepunkt der Altersklasse U20 die Goldmedaille der WM.</p>
            <p>Im Jahr 2012 macht Alex sein Abitur an der Eliteschule des Sports in Kaiserslautern. Danach beginnt ein neuer Lebensabschnitt: er wird in die Männer-Nationalmannschaft aufgenommen und zieht an den Olympiastützpunkt in Köln.
              Gleichzeitig wird Alex Sportsoldat bei der Bundeswehr.</p>

