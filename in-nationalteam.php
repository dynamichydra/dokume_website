            <h2>ZEIT IN DER NATIONALMANNSCHAFT DER MÄNNER</h2>

            <p>Seit dem Jahr 2012 ist Alexander Mitglied der Judo-Nationalmannschaft der Männer. Dort konnte er sich schnell etablieren und belegte einen starken 5. Platz bei den Europameisterschaften im Jahr 2012. Nun kämpft er auch in der
              Gewichtsklasse bis 81kg, welche zu den am stärksten besetzen Gewichtsklassen beim Judo zählt.</p>
            <p>Im Jahr 2013 belegte er den dritten Platz bei der Weltmeisterschaft in Rio de Janeiro mit der Mannschaft.</p>
            <p>2014 startet mit einem starken 3.Platz beim Heim-Grandprix in Düsseldorf, es folgt die Silbermedaille bei dem Grand Prix in Samsun und am Ende des Jahres das Highlight: Bronze beim prestigeträchtigen Kano-Cup und Grand Slam in Tokyo.</p>
            <p>2015 startet erfolgreich mit dem Gewinn der Gold-Medaille bei den Deutschen Meisterschaften. Im Finale verletzt sich Alexander und muss leider 2 Monate pausieren. Er steigt wieder stark ein und erzielt Bronze bei den
              Europameisterschaften und European Games in Baku. Es folgt Bronze beim Grand-Slam in Tyumen.</p>
            <p>Seit dem Jahr 2016 kämpft Alexander für die Bundesliga-Mannschaft in Hamburg. Nach einem guten 5.Platz beim Grand Prix in Havanna erkrankt Alexander an dem lebensbedrohlichen Dengue-Fieber und fällt für einige wichtige Turniere im
              Olympia-Jahr aus.</p>
            <p>Ein Rippenbruch kurz vor der Europameisterschaft in Russland bedeutet das Ende des Olympischen Traums in Rio. Das Jahr nutzt Alex, um sich am Ellbogen operieren zu lassen.</p>
            <p>Anfang 2017 und damit dem Start des neuen Olympischen Zyklus, wechselt der Bundestrainer und Alex fängt wieder an, unter Richard Trautmann zu trainieren. Trautmann war damals der Bundestrainer, als Alex 2010 Junioren-Weltmeister
              wurde.</p>
            <p>Nachdem das Seuchenjahr 2016 beendet Jahr, ist der Start von 2017 etwas holprig. Eine Knieverletzung zwingt Alex zu einer erneuten Pause. Den Einstieg findet er im April mit einem 3-wöchigen Konditions-Camp auf Teneriffa, gefolgt von
              einem 3,5-wöchigen Trainingslager in Japan und Korea.</p>
            <p>Nach weiteren Trainingslagern in Spanien, Frankreich und Deutschland, rückt die WM in Budapest näher. Einen einzigen Testwettkampf bestreitet Alex in Celje/SLO, bei dem er den ersten Platz belegt.</p>
            <p>Als Außenseiter, auf der Weltrangliste 124 startet Alex ins Turnier. Das Los ist auf dem ersten Blick nicht auf seiner Seite: der mehrmalige WM-Medaillengewinner Valois-Fortier (CAN), der amtierende Europameister Khubetsov (RUS), der
              Lokalmatador Csoknyai,der Olympiasieger Khalmurzaev (RUS) und der Olympia-Fünfte von Rio Marconcini (ITA) stehen auf dem Programm. Keine leichte Aufgabe, die Alex am Ende bewältigen muss. Nachdem er alle besiegt hat, steht er im Finale
              um die Gold-Medaille: nach etwas mehr als 2 Minuten hat er es geschafft: Durch einen Würger gewinnt Alex die erste Gold-Medaille seit 14 Jahren für den Deutschen Judo-Bund. Es folgen viele Termine, Treffen und Fernsehtermine.</p>
            <p>Eine Schambeinentzündung zwingt Alex zu einer mehr als 6-monatigen Trainingspause. Hoch motiviert steigt er im Mai wieder ins Geschehen ein und fliegt nach Japan auf ein 3-wöchiges Trainingslager. Erneut folgen Trainingslager in
              Spanien, Weißrussland und Deutschland, bevor es Ende September zur WM nach Baku geht.</p>
            <p>Die Mission lautet: Titel verteidigen und Plan B lautet: eine Medaille holen. Als amtierender Weltmeister ist Alex der Gejagte, jeder möchte ihm seinen Titel streitig machen. Am Wettkampftag muss sich Alex lediglich einem Kämpfer im
              Viertel-Finale geschlagen geben. Über die Trostrunde kämpft er sich zurück und gewinnt im innerdeutschen Duell gegen Dominic Ressel die Bronze-Medaille.</p>
            <p>Somit hat er zwei Jahre hintereinander eine Medaille bei dem ranghöchsten Turnier der IJF (International Judo Federation) erkämpft. Bis zu den Olympischen Spielen 2020 folgt nur noch eine WM im Jahr 2019. Klares Ziel: eine Medaille zu
              erkämpfen.</p>


